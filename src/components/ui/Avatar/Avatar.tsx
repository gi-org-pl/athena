function Avatar({
  className,
  src,
  alt = "",
  size,
  fallback = "icon",
  name = "",
  gender = "male",
  color = "text-gi-red",
  dataTestId,
  ...props
}: AvatarProps) {
  const [hasError, setHasError] = React.useState(false);
  const showImage = src && !hasError;

  return (
    <div
      data-slot="avatar"
      data-test-id={dataTestId}
      className={cn(avatarVariants({ size, className }))}
      {...props}
    >
      {showImage ? (
        <img
          src={src}
          alt={alt || name || "User avatar"}
          className="h-full w-full object-cover transition-opacity duration-300 ease"
          onError={() => setHasError(true)}
        />
      ) : (
        <div
          className="flex h-full w-full items-center justify-center font-medium text-gi-primary"
          aria-label={name || "User avatar fallback"}
        >
          {fallback === "initials" ? (
            getInitials(name)
          ) : (
            <div className={cn("h-full w-full", color)}>
              {gender === "female" ? <FemaleIcon /> : <MaleIcon />}
            </div>
          )}
        </div>
      )}
    </div>
  );
}

export { Avatar, avatarVariants };
